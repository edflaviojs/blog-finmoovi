/**
 * One-off (2026-07-23): remove a nota "gerado automaticamente pela IA" do fim
 * dos termos do glossário existentes (PT/EN/ES), incluindo o separador `---`
 * órfão quando a nota era a única coisa depois dele.
 *
 * O gerador (glossario-auto-diario.js) parou de injetar a nota na mesma data —
 * este script limpa apenas o legado. Uso: node src/scripts/manutencao/remove-nota-ia-glossario.js [--dry-run]
 */

import { readFileSync, writeFileSync, readdirSync } from 'fs';
import { join } from 'path';

const GLOSSARIO_DIR = join(process.cwd(), 'src', 'content', 'glossario');
const DRY_RUN = process.argv.includes('--dry-run');

// As 3 variantes exatas injetadas pelo gerador (o link pode variar de domínio)
const NOTE_STARTS = [
  '*Este termo foi gerado automaticamente pela IA',
  '*This term was automatically generated by AI',
  '*Este término fue generado automáticamente por IA',
];

// Nota no fim do arquivo, precedida do(s) separador(es) `---` que só existiam
// por causa dela (qualidade.md tinha `---` duplicado, por isso o `(?:...)+`)
const NOTE_REGEX = new RegExp(
  `(?:\\n---\\s*)+\\n\\s*(?:${NOTE_STARTS.map(s => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')).join('|')})[^\\n]*\\n?\\s*$`
);

const counts = { pt: 0, en: 0, es: 0 };
const skipped = [];

for (const file of readdirSync(GLOSSARIO_DIR).filter(f => f.endsWith('.md')).sort()) {
  const path = join(GLOSSARIO_DIR, file);
  const original = readFileSync(path, 'utf-8');

  if (!NOTE_STARTS.some(s => original.includes(s))) continue; // arquivo sem nota

  const cleaned = original.replace(NOTE_REGEX, '\n');
  if (cleaned === original) {
    // Tem a nota mas fora do padrão esperado — não mexe, só avisa (auditável)
    skipped.push(file);
    continue;
  }

  if (!DRY_RUN) writeFileSync(path, cleaned, 'utf-8');
  const locale = file.startsWith('en-') ? 'en' : file.startsWith('es-') ? 'es' : 'pt';
  counts[locale]++;
  console.log(`✂️  ${file}`);
}

console.log(`\n${DRY_RUN ? '[dry-run] ' : ''}Notas removidas — PT: ${counts.pt} | EN: ${counts.en} | ES: ${counts.es} | total: ${counts.pt + counts.en + counts.es}`);
if (skipped.length) {
  console.warn(`⚠️ Arquivos com nota fora do padrão (NÃO alterados): ${skipped.join(', ')}`);
}
